#include <stdbool.h>
#include "pico/stdlib.h"

static const uint16_t brightnesses[] = {
  0b0000000000000000, // 0
  0b1000000000000000, // 1
  0b1000000010000000, // 2
  0b1000001000010000, // 3
  0b1000100010001000, // 4
  0b1001001001001000, // 5
  0b1001001001001010, // 6
  0b1010100100101010, // 7
  0b1010101010101010, // 8
  0b1110101010101010, // 9
  0b1110101011101010, // 10
  0b1110101110101110, // 11
  0b1110111011101110, // 12
  0b1111101111011110, // 13
  0b1111101111101111, // 14
  0b1111111101111111, // 15
  0b1111111111111111, // 16
};

bool is_led_on(int brightness, int tick) {
  uint16_t bitset = brightnesses[brightness];
  return (bitset & ((1 << 15) >> tick)) > 0;
}
